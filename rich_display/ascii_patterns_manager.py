from enum import Enum
from typing import List
from os import listdir, path as os_path


class AsciiPatternsManager:

    @classmethod
    def load_patterns(cls, path: str):
        """Load patterns from individual files in a directory."""
        patterns = []
        for filename in listdir(path):
            if filename.endswith('.txt'):
                filepath = os_path.join(path, filename)
                with open(filepath, 'r') as file:
                    pattern = file.read().strip()
                    pattern_name = os_path.splitext(filename)[0].upper()  # Use the filename as the pattern name
                    patterns.append(pattern)
        return patterns

    @classmethod
    def get_all_patterns(cls) -> List[str]:
        return AsciiPatterns.get_all_patterns() + cls.load_patterns(path=r"/home/dev/Dev/python3-interactive-cli/rich_display/ascii_patterns")


